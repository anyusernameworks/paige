#[derive(Clone)]
pub struct Attr {
   attr_name: AttrName,
   value: String,
}

impl std::fmt::Display for Attr {
   fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
      write!(
         f, 
         "{}='{}'",
         self.attr_name.to_str(),
         self.value
      )
   }
}

