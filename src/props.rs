#[derive(Clone)]
pub struct Prop {
   name: String,
   value: String,
}

impl Prop {
   pub fn new<N: Into<String>>(name: N, value: String) -> Self {
      Prop {
         name: name.into(),
         value: value
      }
   }
   
   pub fn value(&mut self, value: String) {
      self.value = value;
   }
}

impl std::fmt::Display for Prop {
   fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
      write!(
         f, 
         "{}: {};",
         self.name,
         self.value
      )
   }
}

